# this program computes the number of seconds in a given number of hours

num_hours = 2 # number of hours
num_seconds = 3600 # number of seconds in 1 hour

print("Hours: ", num_hours) #printing the number of hours
print("Seconds in Hours: ", num_hours * num_seconds) # printing the number of seconds in a given number of hours
print("Seconds in 3 Hour: ", (num_hours+1)*num_seconds)#this is the end of the program that computes the number of seconds in 3 hour
